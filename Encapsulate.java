package com.myjavapractce;

public class Encapsulate {
	
	private int age;
	private int rollno;
	private String name;
	
	public int getAge() {
		return age;
	}
	
	public int getRollNo() {
		return rollno;
	}
	
	public String getName() {
		return name;
	}
	
	public void setAge(int age) {
		this.age=age;
	}
	
	public void setRollNo(int rollno) {
		this.rollno=rollno;
	}
	public void setName(String name) {
		this.name=name;
	}
}
